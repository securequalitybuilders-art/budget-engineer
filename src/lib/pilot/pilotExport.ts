function wrapHtml(title: string, body: string): string {
  return `<!DOCTYPE html><html lang="en"><head><meta charset="UTF-8"><meta name="viewport" content="width=device-width,initial-scale=1"><title>${title}</title><style>${STYLE}</style></head><body>${body}<div class="footer">Generated by Budget Engineer v4.0.0 — ${new Date().toISOString()}</div></body></html>`;
}

function severityBadge(severity: PilotSeverity): string {
  const map: Record<PilotSeverity, string> = {
    blocker: 'badge-red',
    major: 'badge-orange',
    minor: 'badge-amber',
    observation: 'badge-blue',
    enhancement: 'badge-green',
  };
  return `<span class="badge ${map[severity]}">${SEVERITY_LABELS[severity]}</span>`;
}

function statusBadge(status: PilotStatus): string {
  const map: Record<PilotStatus, string> = {
    'new': 'badge-red',
    'under-review': 'badge-orange',
    'action-planned': 'badge-amber',
    'resolved': 'badge-green',
    'accepted-limitation': 'badge-gray',
    'deferred': 'badge-gray',
  };
  return `<span class="badge ${map[status]}">${STATUS_LABELS[status]}</span>`;
}

export function formatPilotIssueLogHtml(
  session: PilotSession,
  observations: PilotObservation[]
): string {
  const sorted = sortBySeverity(observations);
  let table = `<h1>Pilot Issue Log</h1>
<p>Session: ${session.name} | Project: ${session.projectId} | ${observations.length} issues</p>
<table>
<thead><tr>
<th>Severity</th><th>Status</th><th>Domain</th><th>Title</th><th>Reviewer</th><th>Recommendation</th>
</tr></thead><tbody>`;

  for (const o of sorted) {
    table += `<tr>
<td>${severityBadge(o.severity)}</td>
<td>${statusBadge(o.status)}</td>
<td>${DOMAIN_LABELS[o.domain]}</td>
<td><strong>${o.title}</strong><br><span style="font-size:8px;color:#888">${o.description}</span></td>
<td>${o.reviewerName}</td>
<td style="font-size:8px">${o.recommendation || '—'}</td>
</tr>`;
  }

  table += `</tbody></table>`;
  return wrapHtml('Pilot Issue Log', table);
}

export function formatPilotSummaryReportHtml(summary: PilotReviewSummary): string {
  let html = `<h1>Pilot Review Summary</h1>
<p>${summary.sessionName} — Generated ${new Date(summary.generatedAt).toLocaleDateString()}</p>

<h2>Overview</h2>
<table>
<tr><td>Total Observations</td><td>${summary.totalObservations}</td></tr>
<tr><td>Resolution Rate</td><td>${summary.resolutionRate}%</td></tr>
<tr><td>Unresolved Blockers</td><td>${summary.unresolvedBlockers.length}</td></tr>
<tr><td>Recommendation</td><td><strong>${summary.recommendation}</strong></td></tr>
</table>

<h2>By Severity</h2>
<table><thead><tr><th>Severity</th><th>Count</th></tr></thead><tbody>`;

  for (const sev of SEVERITY_ORDER) {
    const count = summary.bySeverity[sev];
    html += `<tr><td>${severityBadge(sev)}</td><td>${count}</td></tr>`;
  }

  html += `</tbody></table>

<h2>By Domain</h2>
<table><thead><tr><th>Domain</th><th>Count</th><th>%</th></tr></thead><tbody>`;

  for (const d of summary.domainConcentration) {
    html += `<tr><td>${DOMAIN_LABELS[d.domain]}</td><td>${d.count}</td><td>${d.pct}%</td></tr>`;
  }

  html += `</tbody></table>

<h2>Resolution Status</h2>
<p>Open: ${summary.openVsResolved.open} | Resolved: ${summary.openVsResolved.resolved} | Total: ${summary.totalObservations}</p>

<h2>Recommendation</h2>
<p><strong>${summary.recommendation}</strong></p>
<p>${summary.recommendationReason}</p>`;

  return wrapHtml('Pilot Review Summary', html);
}

export function formatOpenBlockerReportHtml(observations: PilotObservation[]): string {
  const blockers = getUnresolvedBlockers(observations);
  let html = `<h1>Open Blockers Report</h1>
<p>${blockers.length} unresolved blocker(s)</p>`;

  if (blockers.length === 0) {
    html += `<p style="color:#22c55e">No unresolved blockers. Pilot can proceed.</p>`;
  } else {
    html += `<table><thead><tr><th>Domain</th><th>Title</th><th>Description</th><th>Reviewer</th></tr></thead><tbody>`;
    for (const b of blockers) {
      html += `<tr>
<td>${DOMAIN_LABELS[b.domain]}</td>
<td><strong>${b.title}</strong></td>
<td style="font-size:8px">${b.description}</td>
<td>${b.reviewerName}</td>
</tr>`;
    }
    html += `</tbody></table>`;
  }

  return wrapHtml('Open Blockers Report', html);
}

