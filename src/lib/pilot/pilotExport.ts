export function formatPilotSummaryText(summary: PilotReviewSummary): string {
  const lines: string[] = [
    `Pilot Review Summary — ${summary.sessionName}`,
    `Generated: ${new Date(summary.generatedAt).toLocaleDateString()}`,
    '',
    `Total Observations: ${summary.totalObservations}`,
    `Resolution Rate: ${summary.resolutionRate}%`,
    `Recommendation: ${summary.recommendation}`,
    '',
    `By Severity:`,
    ...SEVERITY_ORDER.filter(s => summary.bySeverity[s] > 0).map(s => `  ${SEVERITY_LABELS[s]}: ${summary.bySeverity[s]}`),
    '',
    `Open: ${summary.openVsResolved.open} | Resolved: ${summary.openVsResolved.resolved}`,
    '',
    `Recommendation Reason: ${summary.recommendationReason}`,
  ];
  return lines.join('\n');
}

