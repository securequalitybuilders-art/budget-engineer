export function formatSessionComparisonHtml(
  sessions: PilotSession[],
  allObservations: PilotObservation[]
): string {
  const sorted = [...sessions].sort(
    (a, b) => new Date(a.startDate).getTime() - new Date(b.startDate).getTime()
  );
  if (sorted.length < 2) {
    const t = formatTrendDataText(buildTrendData(sessions, allObservations));
    return `<h1>Session Comparison</h1><p>At least 2 sessions needed for comparison.</p><p>Trend: ${t}</p>`;
  }

  const first = sorted[0];
  const last = sorted[sorted.length - 1];
  const firstObs = allObservations.filter(o => o.sessionId === first.id);
  const lastObs = allObservations.filter(o => o.sessionId === last.id);
  const firstBlockers = getUnresolvedBlockers(firstObs).length;
  const lastBlockers = getUnresolvedBlockers(lastObs).length;
  const firstResolved = firstObs.filter(o => o.status === 'resolved' || o.status === 'accepted-limitation').length;
  const lastResolved = lastObs.filter(o => o.status === 'resolved' || o.status === 'accepted-limitation').length;
  const firstRate = firstObs.length > 0 ? Math.round((firstResolved / firstObs.length) * 100) : 0;
  const lastRate = lastObs.length > 0 ? Math.round((lastResolved / lastObs.length) * 100) : 0;

  let html = `<h1>Session Comparison</h1>
<p>Comparing earliest ("${first.name}") vs latest ("${last.name}") session</p>

<table>
<thead><tr><th>Metric</th><th>${first.name}</th><th>${last.name}</th><th>Delta</th></tr></thead><tbody>
<tr><td>Observations</td><td>${firstObs.length}</td><td>${lastObs.length}</td><td>${lastObs.length - firstObs.length >= 0 ? '+' : ''}${lastObs.length - firstObs.length}</td></tr>
<tr><td>Unresolved Blockers</td><td>${firstBlockers}</td><td>${lastBlockers}</td><td>${lastBlockers - firstBlockers >= 0 ? '+' : ''}${lastBlockers - firstBlockers}</td></tr>
<tr><td>Resolution Rate</td><td>${firstRate}%</td><td>${lastRate}%</td><td>${lastRate - firstRate >= 0 ? '+' : ''}${lastRate - firstRate}%</td></tr>
`;

  const firstTier = first.readinessContext?.tier ?? null;
  const lastTier = last.readinessContext?.tier ?? null;
  if (firstTier || lastTier) {
    html += `<tr><td>Readiness Tier</td><td>${firstTier ?? '—'}</td><td>${lastTier ?? '—'}</td><td>—</td></tr>`;
  }
  const firstScore = first.readinessContext?.benchmarkScore ?? null;
  const lastScore = last.readinessContext?.benchmarkScore ?? null;
  if (firstScore !== null || lastScore !== null) {
    const delta = firstScore !== null && lastScore !== null ? lastScore - firstScore : 0;
    html += `<tr><td>Benchmark Score</td><td>${firstScore !== null ? firstScore + '%' : '—'}</td><td>${lastScore !== null ? lastScore + '%' : '—'}</td><td>${delta >= 0 ? '+' : ''}${delta}%</td></tr>`;
  }

  html += `</tbody></table>`;

  const trend = buildTrendData(sessions, allObservations);
  html += `<h2>Overall Trend</h2><p>${trend.overallAssessment}</p>`;

  return html;
}

export function formatMultiSessionSummaryText(
  sessions: PilotSession[],
  allObservations: PilotObservation[]
): string {
  const lines: string[] = [
    'Multi-Session Pilot Summary',
    `${sessions.length} sessions, ${allObservations.length} total observations`,
    '',
  ];

  const sorted = [...sessions].sort(
    (a, b) => new Date(a.startDate).getTime() - new Date(b.startDate).getTime()
  );
  for (const session of sorted) {
    const sessionObs = allObservations.filter(o => o.sessionId === session.id);
    const blockers = getUnresolvedBlockers(sessionObs).length;
    const resolved = sessionObs.filter(o => o.status === 'resolved' || o.status === 'accepted-limitation').length;
    const rate = sessionObs.length > 0 ? Math.round((resolved / sessionObs.length) * 100) : 0;
    const tier = session.readinessContext?.tier ?? '—';
    lines.push(`  ${session.name}: ${sessionObs.length} obs, ${blockers} blockers, ${rate}% resolved, tier: ${tier}`);
  }

  const trend = buildTrendData(sessions, allObservations);
  lines.push('', 'Overall Trend:', `  ${trend.overallAssessment}`);

  return lines.join('\n');
}

