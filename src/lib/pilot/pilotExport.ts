export function formatMultiSessionSummaryHtml(
  sessions: PilotSession[],
  allObservations: PilotObservation[]
): string {
  const trend = buildTrendData(sessions, allObservations);
  let html = `<h1>Multi-Session Pilot Summary</h1>
<p>${sessions.length} sessions, ${allObservations.length} total observations</p>

<h2>Session Overview</h2>
<table><thead><tr>
<th>Session</th><th>Status</th><th>Observations</th><th>Blockers</th><th>Major</th><th>Resolved</th><th>Rate</th><th>Readiness Tier</th>
</tr></thead><tbody>`;

  const sorted = [...sessions].sort(
    (a, b) => new Date(a.startDate).getTime() - new Date(b.startDate).getTime()
  );
  for (const session of sorted) {
    const sessionObs = allObservations.filter(o => o.sessionId === session.id);
    const blockers = getUnresolvedBlockers(sessionObs);
    const resolved = sessionObs.filter(o => o.status === 'resolved' || o.status === 'accepted-limitation').length;
    const major = sessionObs.filter(o => o.severity === 'major').length;
    const rate = sessionObs.length > 0 ? Math.round((resolved / sessionObs.length) * 100) : 0;
    const tier = session.readinessContext?.tier ?? '—';
    html += `<tr>
<td><strong>${session.name}</strong></td>
<td>${session.status}</td>
<td>${sessionObs.length}</td>
<td>${blockers.length}</td>
<td>${major}</td>
<td>${resolved}</td>
<td>${rate}%</td>
<td>${tier}</td>
</tr>`;
  }

  html += `</tbody></table>

<h2>Overall Trend</h2>
<p>${trend.overallAssessment}</p>`;

  return html;
}

